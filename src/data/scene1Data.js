// 计算主食占比（谷物直消费比重）
export function calcStapleShare(year) {
  const data = dietStructure;
  const index = d3Bisect(data, year);
  if (index <= 0) return data[0].grain_direct;
  if (index >= data.length) return data[data.length - 1].grain_direct;
  const prev = data[index - 1];
  const next = data[index];
  const t = (year - prev.year) / (next.year - prev.year);
  return prev.grain_direct + (next.grain_direct - prev.grain_direct) * t;
}

// 二分查找辅助
function d3Bisect(arr, year) {
  let lo = 0, hi = arr.length;
  while (lo < hi) {
    const mid = (lo + hi) >> 1;
    if (arr[mid].year < year) lo = mid + 1;
    else hi = mid;
  }
  return lo;
}

